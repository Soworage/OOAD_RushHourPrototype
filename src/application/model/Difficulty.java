package application.model;

/**
 * Enum für die verschiedenen Schwierigkeitsgrade in der Anwendung.
 *
 * <p>Hauptverantwortlicher:</p>
 * <ul>
 *     <li>Alex Mihel</li>
 * </ul>
 */
public enum Difficulty {
    /**
     * Leichter Schwierigkeitsgrad.
     * Geeignet für Anfänger oder für eine entspannte Spielweise.
     */
    EASY,

    /**
     * Mittlerer Schwierigkeitsgrad.
     * Bietet eine ausgewogene Herausforderung.
     */
    MEDIUM,

    /**
     * Schwieriger Schwierigkeitsgrad.
     * Für erfahrene Spieler oder größere Herausforderungen.
     */
    HARD
}
